    public static int Preguntar_Salir(){
        return JOptionPane.showOptionDialog(null, "Estas a punto de salir "
                + "¿Estas Seguro?", "¿Desea Salir?", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Si","No"}, 0);
    }
    
    private void limpiar(){
        txt_editor.setText("");
        modelo.setRowCount(0);
        
    }
    
    public void darleFormaALaTabla(JTable tabla){
        tabla.getColumnModel().getColumn(0).setPreferredWidth(2);
        tabla.getColumnModel().getColumn(1).setPreferredWidth(2);
        tabla.getColumnModel().getColumn(2).setPreferredWidth(100);
        tabla.getColumnModel().getColumn(3).setPreferredWidth(10);
        tabla.getColumnModel().getColumn(4).setPreferredWidth(100);
        
    }
