    private void txt_analizarTextoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txt_analizarTextoMouseClicked
        ArrayList<Token> lista_token = new ArrayList();
        new analizador (lista_token).analizar(txt_editor.getText());
        
        //rellenar tabla
        tbl_resultados.setModel(modelo);
        darleFormaALaTabla(tbl_resultados);
        for(int i=0; i<lista_token.size(); i++){
            Vector<Object> fila = new Vector<>();
            
            fila.add(lista_token.get(i).getFila());
            fila.add(lista_token.get(i).getColumna());
            fila.add(lista_token.get(i).getLexema());
            fila.add(lista_token.get(i).getNumero_token());
            fila.add(lista_token.get(i).getTipo());
            
            modelo.addRow(fila);
        }
    }//GEN-LAST:event_txt_analizarTextoMouseClicked

